    public int findLucky(int[] arr) {
        Map<Integer,Integer> map = new HashMap<>();

        for(int i=0; i<arr.length; i++){
            map.put(arr[i], map.getOrDefault(arr[i],0)+1);
        }

        List<Integer> luckyList = new ArrayList<>();
        for(Integer key:map.keySet()){
            if(Objects.equals(key, map.get(key))){
                luckyList.add(key);
            }
        }


        int largestLuckyNumber =0;
        for(Integer i:luckyList){
            largestLuckyNumber = Math.max(largestLuckyNumber,i);
        }

        return largestLuckyNumber==0?-1:largestLuckyNumber;
    }
